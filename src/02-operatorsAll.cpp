#include <iostream>

using namespace std;

void operatorTest() {
  int a = 10;
  int b = 2;

  // 加、减、乘、除
  cout << "a + b =" << a + b << endl;
  cout << "a * b =" << a * b << endl;
  cout << "a - b =" << a - b << endl;
  cout << "a / b =" << a / b << endl;

  // 小数之间的运算
  double d1 = 0.5;
  double d2 = 0.22;

  cout << "d1 / d2 = " << d1 / d2 << endl;
};
